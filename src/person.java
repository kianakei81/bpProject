

public class person {

    public String type;
    public String username;
    public String password;
    public Long money;
    public Integer verify;

    public person(String type, String username, String password, Long money, Integer verify) {
        this.type = type;
        this.username = username;
        this.password = password;
        this.money = money;
        this.verify = verify;
    }

}
